def predict_rent(house_type, locality, city, furnishing, area, beds, bathrooms, balconies):
    """
    Predicts monthly rent using the pre-trained CatBoost model.
    """
    # Create input dataframe
    input_data = {
        'house_type': [house_type],
        'locality': [locality],
        'city': [city],
        'furnishing': [furnishing],
        'area': [area],
        'beds': [beds],
        'bathrooms': [bathrooms],
        'balconies': [balconies]
    }
    input_df = pd.DataFrame(input_data)

    # Feature engineering - area_bucket
    area_bins = [0, 300, 600, 900, 1200, 2000, 5000, 100000]
    area_val = input_df['area'].values[0]
    area_bucket = np.digitize([area_val], area_bins)[0] - 1
    input_df['area_bucket'] = max(0, area_bucket)

    # Create rent_psf_bucket using similar properties from training data
    similar_areas = df[df['area_bucket'] == input_df['area_bucket'].values[0]]
    if len(similar_areas) > 0:
        median_rent_psf = similar_areas['rent_psf'].median()
        rent_psf_bins = [0, 10, 20, 30, 40, 50, 60, 80, 100, 200]
        rent_psf_bucket = np.digitize([median_rent_psf], rent_psf_bins)[0]
    else:
        rent_psf_bucket = 3
    input_df['rent_psf_bucket'] = rent_psf_bucket

    # Ensure categorical columns are string type
    for col in ['house_type', 'locality', 'city', 'furnishing']:
        input_df[col] = input_df[col].astype(str)

    # Make prediction
    try:
        predicted_rent = model.predict(input_df)[0]
        predicted_rent = max(1000, min(predicted_rent, 500000))
        return round(predicted_rent, 2)
    except Exception as e:
        print(f"Prediction error: {e}")
        # Fallback: simple area-based estimate (₹35-₹60 per sq ft based on city)
        city_rates = {'Mumbai': 60, 'Delhi': 45, 'Bangalore': 50, 'Chennai': 35,
                      'Kolkata': 30, 'Hyderabad': 40, 'Pune': 42, 'Ahmedabad': 32}
        rate = city_rates.get(city, 40)
        return round(area * rate, 2)

